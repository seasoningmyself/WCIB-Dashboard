CREATE FUNCTION "enforce_audit_events_append_only"()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
	RAISE EXCEPTION 'audit events are append-only'
		USING ERRCODE = '55000',
			CONSTRAINT = 'audit_events_append_only';
	RETURN NULL;
END;
$$;
